def print_tables(results: List[CaseResult],
                 ref_u: Optional[float],
                 ref_v: Optional[float],
                 ref_source: str,
                 ref_case: Optional[CaseResult]):

    has_ref  = ref_u is not None or ref_v is not None
    W = 86
    WC = 28   # case-name column width

    load_types = list(dict.fromkeys(r.load_type for r in results))

    for lt in load_types:
        group  = [r for r in results if r.load_type == lt]
        active = [r for r in group if not r.is_reference]
        if not group:
            continue

        print()
        print('═' * W)
        print(f'  Load type: {lt.upper()}')
        if has_ref:
            print(f'  Reference: {ref_source}')
            if ref_u is not None:
                line = f'  u_ref = {ref_u:+.8g}'
                if ref_v is not None:
                    line += f'   v_ref = {ref_v:+.8g}'
                print(line)
        print('═' * W)

        if has_ref:
            print(f"  {'Case':<{WC}}  {'N':>4}  {'U (T1)':>13}  {'err_u':>9}  "
                  f"{'slope_u':>8}  {'V (T2)':>13}  {'err_v':>9}  {'slope_v':>8}")
            sep = (f"  {'─'*WC}  {'─'*4}  {'─'*13}  {'─'*9}  "
                   f"{'─'*8}  {'─'*13}  {'─'*9}  {'─'*8}")
        else:
            print(f"  {'Case':<{WC}}  {'N':>4}  {'U (T1)':>13}  {'V (T2)':>13}")
            sep = f"  {'─'*WC}  {'─'*4}  {'─'*13}  {'─'*13}"
        print(sep)

        # Reference row (if it came from the scanned results)
        if ref_case and ref_case in group:
            rc = ref_case
            tag = f'{rc.mesh_type}  N={rc.N}  \u2190 REFERENCE'
            if has_ref:
                print(f"  {tag:<{WC}}  {rc.N:>4}  "
                      f"{_disp(rc.u)}  {'(reference)':>9}  {'':>8}  "
                      f"{_disp(rc.v)}  {'(reference)':>9}  {'':>8}")
            else:
                print(f"  {tag:<{WC}}  {rc.N:>4}  {_disp(rc.u)}  {_disp(rc.v)}")
            print(sep)

        # Remaining cases grouped by mesh type
        for mesh in ('regular', 'distorted', 'unknown'):
            mesh_cases = sorted(
                [r for r in active if r.mesh_type == mesh],
                key=lambda r: r.N or 0)
            if not mesh_cases:
                continue

            for idx, r in enumerate(mesh_cases):
                name = f'{r.mesh_type}  N={r.N}' if r.N else r.stem[:WC]
                if r.parse_error:
                    name += ' !'

                su = sv = ''
                if has_ref and idx > 0 and mesh == 'regular':
                    prev = mesh_cases[idx - 1]
                    if ref_u is not None and r.u is not None and prev.u is not None:
                        su = _slope_str(prev, r, 'u', ref_u)
                    if ref_v is not None and r.v is not None and prev.v is not None:
                        sv = _slope_str(prev, r, 'v', ref_v)

                if has_ref:
                    print(f"  {name:<{WC}}  {str(r.N or '?'):>4}  "
                          f"{_disp(r.u)}  {_pct(r.u, ref_u)}  {su:>8}  "
                          f"{_disp(r.v)}  {_pct(r.v, ref_v)}  {sv:>8}")
                else:
                    print(f"  {name:<{WC}}  {str(r.N or '?'):>4}  "
                          f"{_disp(r.u)}  {_disp(r.v)}")

                if r.parse_error:
                    print(f"  {'':>{WC}}       *** {r.parse_error.strip()}")

            print()   # blank row between mesh groups

    print()


