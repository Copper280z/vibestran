def write_results_txt(results: List[CaseResult],
                      ref_u: Optional[float],
                      ref_v: Optional[float],
                      ref_source: str,
                      ref_case: Optional[CaseResult],
                      out_path: str):
    W = 80
    lines = ['=' * W,
             'MITC4 BENCHMARK — POINT A DISPLACEMENTS',
             '=' * W,
             'Generated by mitc4_postprocess.py',
             f'Reference source: {ref_source}']
    if ref_u is not None:
        lines.append(f'  u_ref = {ref_u:+.10g}')
    if ref_v is not None:
        lines.append(f'  v_ref = {ref_v:+.10g}')
    if ref_case:
        lines.append(f'  (N={ref_case.N} regular mesh used as reference; '
                     f'excluded from error columns)')
    lines.append('')

    PAPER = {2:  (19.12, 7.59, 11.53, 23.29, 6.96,  9.53),
             4:  ( 6.92, 3.96,  0.44,  6.08, 2.95,  1.37),
             8:  ( 2.28, 1.38,  0.25,  1.93, 1.04,  0.09),
             16: ( 0.78, 0.34,  0.12,  0.83, 0.42,  0.08)}
    lines.append('Paper error tables (Choi & Lee 2022, distorted mesh, % error vs 64x64):')
    lines.append(f'  {"N":>4}  {"u:Q4":>8}  {"u:MITC4":>9}  {"u:Impr":>8}'
                 f'  {"v:Q4":>8}  {"v:MITC4":>9}  {"v:Impr":>8}')
    lines.append('  ' + '-' * 62)
    for N, v in sorted(PAPER.items()):
        lines.append(f'  {N:>4}  {v[0]:>7.2f}%  {v[1]:>8.2f}%  {v[2]:>7.2f}%'
                     f'  {v[3]:>7.2f}%  {v[4]:>8.2f}%  {v[5]:>7.2f}%')
    lines += ['', '=' * W, 'RESULTS', '=' * W, '']

    load_types = list(dict.fromkeys(r.load_type for r in results))
    for lt in load_types:
        group  = [r for r in results if r.load_type == lt]
        active = [r for r in group if not r.is_reference]

        lines.append(f'--- Load type: {lt} ---')
        lines.append('')

        hdr = (f'  {"File":<36}  {"N":>4}  {"mesh":>10}  '
               f'{"U (T1)":>13}  {"V (T2)":>13}  '
               f'{"err_u%":>9}  {"slope_u":>8}  {"err_v%":>9}  {"slope_v":>8}')
        lines += [hdr, '  ' + '-' * (len(hdr) - 2)]

        if ref_case and ref_case in group:
            rc = ref_case
            lines.append(
                f'  {os.path.basename(rc.f06_path):<36}  {rc.N:>4}  '
                f'{rc.mesh_type:>10}  {_disp(rc.u)}  {_disp(rc.v)}'
                f'  {"(ref)":>9}  {"":>8}  {"(ref)":>9}  {"":>8}')
            lines.append('  ' + '-' * (len(hdr) - 2))

        for mesh in ('regular', 'distorted', 'unknown'):
            mesh_cases = sorted(
                [r for r in active if r.mesh_type == mesh],
                key=lambda r: r.N or 0)
            prev = None
            for r in mesh_cases:
                fname = os.path.basename(r.f06_path)[:36]
                eu = _pct(r.u, ref_u).strip() if ref_u is not None else '—'
                ev = _pct(r.v, ref_v).strip() if ref_v is not None else '—'
                su = sv = ''
                if prev is not None and mesh == 'regular':
                    if ref_u is not None and r.u is not None and prev.u is not None:
                        su = _slope_str(prev, r, 'u', ref_u)
                    if ref_v is not None and r.v is not None and prev.v is not None:
                        sv = _slope_str(prev, r, 'v', ref_v)
                lines.append(
                    f'  {fname:<36}  {str(r.N or "?"):>4}  {r.mesh_type:>10}  '
                    f'{_disp(r.u)}  {_disp(r.v)}  '
                    f'{eu:>9}  {su:>8}  {ev:>9}  {sv:>8}')
                if r.parse_error:
                    lines.append(f'    ERROR: {r.parse_error.strip()}')
                prev = r
            if mesh_cases:
                lines.append('')

    lines += ['=' * W, 'RAW VALUES (full precision)', '=' * W]
    for r in results:
        tag = ' [REFERENCE]' if r.is_reference else ''
        lines += [f'{r.stem}{tag}:',
                  f'  Point A node = {r.point_a_nid}',
                  f'  U (T1) = {r.u!r}',
                  f'  V (T2) = {r.v!r}']
        if r.parse_error:
            lines.append(f'  ERRORS: {r.parse_error.strip()}')
    lines.append('')

    with open(out_path, 'w') as f:
        f.write('\n'.join(lines) + '\n')
    print(f'Results written to: {out_path}')


