def main():
    parser = argparse.ArgumentParser(
        description='Post-process Nastran F06 results for the Choi & Lee (2022) '
                    'clamped plate benchmark.',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=textwrap.dedent("""
        Reference priority
        ------------------
          1. --ref-u / --ref-v         explicit displacement values
          2. --ref-from-file           specific F06 file
          3. auto (default)            largest regular N >= --ref-threshold
                                       (default: 64) found in the directory;
                                       that case is excluded from error plots
                                       and labelled REFERENCE in all output
          4. exact 2D elasticity       formula embedded in BDF comments

        Examples
        --------
          python mitc4_postprocess.py results/
          python mitc4_postprocess.py results/ --ref-threshold 32
          python mitc4_postprocess.py results/ --ref-u 3.0 --ref-v -3.225
          python mitc4_postprocess.py results/ --ref-from-file results/regular_N64.f06
          python mitc4_postprocess.py results/ --no-plots
        """))

    parser.add_argument('directory',
                        help='Directory containing .f06 (and optionally .bdf) files')
    parser.add_argument('--ref-u', type=float, default=None,
                        help='Reference U displacement at Point A')
    parser.add_argument('--ref-v', type=float, default=None,
                        help='Reference V displacement at Point A')
    parser.add_argument('--ref-from-file', default=None,
                        help='F06 file to use as the reference solution')
    parser.add_argument('--ref-threshold', type=int, default=64,
                        help='Minimum N for auto-reference (default: 64)')
    parser.add_argument('--no-plots', action='store_true',
                        help='Skip plot generation')
    parser.add_argument('--out-dir', default=None,
                        help='Output directory (default: same as input directory)')
    parser.add_argument('--out-txt', default='results.txt',
                        help='Name of the text summary file (default: results.txt)')

    args = parser.parse_args()

    if not os.path.isdir(args.directory):
        print(f'ERROR: {args.directory!r} is not a directory.')
        sys.exit(1)

    out_dir = args.out_dir or args.directory

    print(f'\nScanning {args.directory!r} for .f06 files ...\n')
    results = scan_directory(args.directory)
    if not results:
        print('No results found.')
        sys.exit(0)

    print(f'Found {len(results)} result file(s):\n')
    for r in results:
        status = 'OK' if r.u is not None else 'ERR'
        print(f'  [{status}]  {os.path.basename(r.f06_path):<45}  '
              f'mesh={r.mesh_type}  N={r.N}  load={r.load_type}')

    ref_u, ref_v, ref_source, ref_case = pick_reference(
        results, args.ref_u, args.ref_v,
        args.ref_from_file, args.ref_threshold)

    print()
    print(f'Reference: {ref_source}')
    if ref_u is not None:
        print(f'  u_ref = {ref_u:+.8g}')
    if ref_v is not None:
        print(f'  v_ref = {ref_v:+.8g}')
    if ref_case:
        print(f'  (N={ref_case.N} case excluded from error/convergence columns)')

    print_tables(results, ref_u, ref_v, ref_source, ref_case)

    os.makedirs(out_dir, exist_ok=True)
    txt_path = os.path.join(out_dir, args.out_txt)
    write_results_txt(results, ref_u, ref_v, ref_source, ref_case, txt_path)

    if not args.no_plots:
        print('\nGenerating plots ...')
        make_plots(results, ref_u, ref_v, ref_source, ref_case, out_dir)

    print('\nDone.')


if __name__ == '__main__':
    main()
