# Reference selection
# ---------------------------------------------------------------------------

def pick_reference(
        results: List[CaseResult],
        ref_u: Optional[float],
        ref_v: Optional[float],
        ref_from_file: Optional[str],
        ref_threshold: int = 64,
) -> Tuple[Optional[float], Optional[float], str, Optional[CaseResult]]:
    """
    Returns (u_ref, v_ref, source_description, ref_CaseResult_or_None).

    Priority:
      1. --ref-u / --ref-v
      2. --ref-from-file
      3. Auto: regular-mesh result with largest N >= ref_threshold
      4. Exact formula from BDF comments
      5. No reference
    """
    if ref_u is not None or ref_v is not None:
        return ref_u, ref_v, 'user-supplied', None

    if ref_from_file:
        bdf_ref = ref_from_file.replace('.f06', '.bdf')
        r_tmp = CaseResult()
        r_tmp.f06_path = ref_from_file
        if os.path.exists(bdf_ref):
            parse_bdf_metadata(bdf_ref, r_tmp)
        disps = parse_f06_displacements(ref_from_file)
        if r_tmp.point_a_nid and r_tmp.point_a_nid in disps:
            u, v = disps[r_tmp.point_a_nid]
            return u, v, f'F06 file: {os.path.basename(ref_from_file)}', None
        print(f'WARNING: could not extract displacement from {ref_from_file}')

    # Auto: largest regular N >= threshold
    candidates = [r for r in results
                  if r.mesh_type == 'regular'
                  and r.N is not None and r.N >= ref_threshold
                  and r.u is not None]
    if candidates:
        ref_case = max(candidates, key=lambda r: r.N)
        ref_case.is_reference = True
        src = (f'auto: regular N={ref_case.N} '
               f'(largest N \u2265 {ref_threshold} in results)')
        return ref_case.u, ref_case.v, src, ref_case

    # Exact formula from BDF comments
    us = [r.exact_u for r in results if r.exact_u is not None]
    vs = [r.exact_v for r in results if r.exact_v is not None]
    if us:
        return us[0], (vs[0] if vs else None), \
               'exact 2D elasticity (from BDF comments)', None

    return None, None, 'none', None


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _pct(val: Optional[float], ref: Optional[float]) -> str:
    if val is None or ref is None or abs(ref) < 1e-30:
        return '    —    '
    return f'{abs(val - ref) / abs(ref) * 100:8.3f}%'


def _disp(val: Optional[float]) -> str:
    if val is None:
        return '      —      '
    return f'{val:+13.6E}'


def _slope_str(r0: CaseResult, r1: CaseResult,
               comp: str, ref: float) -> str:
    """Log-log convergence slope between two consecutive mesh sizes."""
    e0 = abs(getattr(r0, comp) - ref) if getattr(r0, comp) is not None else 0.0
    e1 = abs(getattr(r1, comp) - ref) if getattr(r1, comp) is not None else 0.0
    if e0 < 1e-30 or e1 < 1e-30 or not r0.N or not r1.N or r0.N == r1.N:
        return ''
    try:
        s = math.log(e1 / e0) / math.log(r0.N / r1.N)
        return f'{s:+.2f}'
    except (ValueError, ZeroDivisionError):
        return ''


def _short(s: str, n: int = 44) -> str:
    return s[:n] + '\u2026' if len(s) > n else s


