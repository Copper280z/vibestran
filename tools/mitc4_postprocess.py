# Directory scanner
# ---------------------------------------------------------------------------

def scan_directory(directory: str) -> List[CaseResult]:
    results = []
    f06_files = sorted(p for p in os.listdir(directory)
                       if p.lower().endswith('.f06'))
    if not f06_files:
        print(f'  No .f06 files found in {directory!r}')
        return results

    for fname in f06_files:
        stem     = fname[:-4]
        f06_path = os.path.join(directory, fname)
        bdf_path = os.path.join(directory, stem + '.bdf')
        if not os.path.exists(bdf_path):
            bdf_path = None

        r = CaseResult()
        r.f06_path = f06_path
        r.bdf_path = bdf_path
        r.stem     = stem

        # Fallback metadata from filename
        m = re.search(r'(regular|distorted)', stem, re.IGNORECASE)
        if m:
            r.mesh_type = m.group(1).lower()
        m = re.search(r'[_\-]N(\d+)', stem, re.IGNORECASE)
        if m:
            r.N = int(m.group(1))
        m = re.search(r'(traction|couple)', stem, re.IGNORECASE)
        if m:
            r.load_type = m.group(1).lower()

        if bdf_path:
            parse_bdf_metadata(bdf_path, r)

        disps = parse_f06_displacements(f06_path)

        if r.point_a_nid is None:
            r.parse_error = (r.parse_error or '') + \
                ' [Point A node ID not found in BDF]'
        elif r.point_a_nid not in disps:
            r.parse_error = (r.parse_error or '') + \
                f' [Node {r.point_a_nid} not in F06 displacement table]'
        else:
            r.u, r.v = disps[r.point_a_nid]

        results.append(r)

    results.sort(key=lambda r: r.sort_key)
    return results


