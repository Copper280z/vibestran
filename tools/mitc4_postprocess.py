def make_plots(results: List[CaseResult],
               ref_u: Optional[float],
               ref_v: Optional[float],
               ref_source: str,
               ref_case: Optional[CaseResult],
               out_dir: str):
    try:
        import matplotlib
        matplotlib.use('Agg')
        import matplotlib.pyplot as plt
    except ImportError:
        print('WARNING: matplotlib not available — skipping plots.')
        print('         Install with: pip install matplotlib')
        return

    os.makedirs(out_dir, exist_ok=True)

    C_REG   = '#2166ac'
    C_DIST  = '#d6604d'
    C_REF   = '#1a1a1a'
    C_PAPER = '#888888'
    C_SLOPE = '#cccccc'
    MK_REG  = 'o'
    MK_DIST = 's'

    load_types = list(dict.fromkeys(r.load_type for r in results))

    for lt in load_types:
        group    = [r for r in results if r.load_type == lt]
        plotable = [r for r in group if not r.is_reference]

        regular   = sorted([r for r in plotable
                             if r.mesh_type == 'regular' and r.N and r.u is not None],
                            key=lambda r: r.N)
        distorted = sorted([r for r in plotable
                             if r.mesh_type == 'distorted' and r.N and r.u is not None],
                            key=lambda r: r.N)

        if not regular and not distorted:
            continue

        suffix = f'_{lt}' if len(load_types) > 1 else ''

        # ── Plot 1: absolute displacements vs N ──────────────────────────────
        fig, axes = plt.subplots(1, 2, figsize=(12, 5))
        fig.suptitle(
            f'Point A Displacements — {lt.capitalize()} load\n'
            f'Choi & Lee (2022) Benchmark',
            fontsize=13, fontweight='bold', y=1.01)

        for ax, comp, ref_val, ylabel in [
                (axes[0], 'u', ref_u, 'U  (horizontal, T1)'),
                (axes[1], 'v', ref_v, 'V  (vertical,   T2)')]:

            Ns_r = [r.N for r in regular]
            Vs_r = [getattr(r, comp) for r in regular]
            Ns_d = [r.N for r in distorted]
            Vs_d = [getattr(r, comp) for r in distorted]

            if Ns_r:
                ax.plot(Ns_r, Vs_r, color=C_REG, marker=MK_REG,
                        linewidth=1.8, markersize=7, label='Regular mesh')
            if Ns_d:
                ax.plot(Ns_d, Vs_d, color=C_DIST, marker=MK_DIST,
                        linewidth=1.8, markersize=7, linestyle='--',
                        label='Distorted mesh')

            if ref_val is not None:
                ax.axhline(ref_val, color=C_REF, linewidth=1.2, linestyle=':',
                           label=f'Reference ({_short(ref_source, 28)})')
            if ref_case and ref_case.load_type == lt:
                rv = getattr(ref_case, comp)
                if rv is not None:
                    ax.plot(ref_case.N, rv, marker='*', markersize=14,
                            color=C_REF, zorder=5,
                            label=f'N={ref_case.N} (reference)')

            for r in regular + distorted:
                val = getattr(r, comp)
                if val is not None:
                    ax.annotate(f'{val:.4f}', xy=(r.N, val),
                                xytext=(4, 4), textcoords='offset points',
                                fontsize=7.5, color='#444444')

            all_Ns = sorted(set(Ns_r + Ns_d))
            ax.set_xlabel('Mesh size  N', fontsize=11)
            ax.set_ylabel(ylabel, fontsize=11)
            ax.set_title(ylabel, fontsize=11)
            ax.legend(fontsize=9, loc='best')
            ax.grid(True, linestyle=':', alpha=0.5)
            if all_Ns:
                ax.set_xticks(all_Ns)

        fig.tight_layout()
        path = os.path.join(out_dir, f'displacements{suffix}.png')
        fig.savefig(path, dpi=150, bbox_inches='tight')
        plt.close(fig)
        print(f'  Plot saved: {path}')

        # ── Plot 2: log-log convergence ───────────────────────────────────────
        if ref_u is None and ref_v is None:
            continue

        PAPER_U = {2: 19.12, 4: 6.92, 8: 2.28, 16: 0.78}
        PAPER_V = {2: 23.29, 4: 6.08, 8: 1.93, 16: 0.83}

        fig, axes = plt.subplots(1, 2, figsize=(12, 5))
        fig.suptitle(
            f'Convergence — Relative Error at Point A\n'
            f'{lt.capitalize()} load  |  Ref: {_short(ref_source, 50)}',
            fontsize=13, fontweight='bold', y=1.01)

        for ax, comp, ref_val, paper_errs, ylabel in [
                (axes[0], 'u', ref_u, PAPER_U,
                 '|u_h \u2212 u_ref| / |u_ref|  (%)'),
                (axes[1], 'v', ref_v, PAPER_V,
                 '|v_h \u2212 v_ref| / |v_ref|  (%)')]:

            if ref_val is None or abs(ref_val) < 1e-30:
                ax.text(0.5, 0.5, 'No reference value',
                        transform=ax.transAxes, ha='center', va='center',
                        fontsize=11, color='#888888')
                continue

            def err_pts(cases):
                return [(1.0 / r.N,
                         abs(getattr(r, comp) - ref_val) / abs(ref_val) * 100)
                        for r in cases
                        if getattr(r, comp) is not None and r.N]

            errs_r = err_pts(regular)
            errs_d = err_pts(distorted)

            if errs_r:
                xs, ys = zip(*errs_r)
                ax.loglog(xs, ys, color=C_REG, marker=MK_REG,
                          linewidth=1.8, markersize=7, label='Regular mesh')
                for (x, y), r in zip(errs_r, regular):
                    ax.annotate(f'N={r.N}', xy=(x, y),
                                xytext=(4, 4), textcoords='offset points',
                                fontsize=7.5, color='#444444')

            if errs_d:
                xs, ys = zip(*errs_d)
                ax.loglog(xs, ys, color=C_DIST, marker=MK_DIST,
                          linewidth=1.8, markersize=7, linestyle='--',
                          label='Distorted mesh')
                for (x, y), r in zip(errs_d, distorted):
                    ax.annotate(f'N={r.N}', xy=(x, y),
                                xytext=(4, -10), textcoords='offset points',
                                fontsize=7.5, color='#444444')

            # Paper Q4 curve
            paper_pts = sorted((1.0 / N, e) for N, e in paper_errs.items())
            if paper_pts:
                xs_p, ys_p = zip(*paper_pts)
                ax.loglog(xs_p, ys_p, color=C_PAPER, marker='^',
                          linewidth=1.2, markersize=6, linestyle='-.',
                          label='Paper: Q4 (Choi & Lee)')

            # O(h²) slope guide
            all_pts = errs_r + errs_d
            if len(all_pts) >= 2:
                spts = sorted(all_pts)
                hm, em = spts[len(spts) // 2]
                h_lo = min(x for x, _ in all_pts) * 0.7
                h_hi = max(x for x, _ in all_pts) * 1.4
                ax.loglog([h_lo, h_hi],
                          [em * (h_lo / hm) ** 2, em * (h_hi / hm) ** 2],
                          color=C_SLOPE, linewidth=1.0, linestyle='--',
                          label='O(h\u00b2) slope')

            ax.set_xlabel('Element size  h = 1/N', fontsize=11)
            ax.set_ylabel(ylabel, fontsize=10)
            ax.set_title(f'{comp.upper()} error vs mesh size', fontsize=11)
            ax.legend(fontsize=9, loc='best')
            ax.grid(True, which='both', linestyle=':', alpha=0.4)

        fig.tight_layout()
        path = os.path.join(out_dir, f'convergence{suffix}.png')
        fig.savefig(path, dpi=150, bbox_inches='tight')
        plt.close(fig)
        print(f'  Plot saved: {path}')

        # ── Plot 3: error bar chart ───────────────────────────────────────────
        ns_all = sorted(set(r.N for r in plotable if r.N and r.u is not None))
        if not ns_all:
            continue

        fig, axes = plt.subplots(1, 2, figsize=(12, 5))
        fig.suptitle(
            f'Relative Error at Point A\n'
            f'{lt.capitalize()} load  |  Ref: {_short(ref_source, 50)}',
            fontsize=13, fontweight='bold', y=1.01)

        bar_w = 0.35

        for ax, comp, ref_val, ylabel in [
                (axes[0], 'u', ref_u, '|u_h \u2212 u_ref| / |u_ref|  (%)'),
                (axes[1], 'v', ref_v, '|v_h \u2212 v_ref| / |v_ref|  (%)')]:

            if ref_val is None or abs(ref_val) < 1e-30:
                ax.text(0.5, 0.5, 'No reference value',
                        transform=ax.transAxes, ha='center', va='center',
                        fontsize=11, color='#888888')
                continue

            def get_err(cases, N):
                r = next((x for x in cases if x.N == N), None)
                if r is None or getattr(r, comp) is None:
                    return None
                return abs(getattr(r, comp) - ref_val) / abs(ref_val) * 100

            xs = list(range(len(ns_all)))
            errs_r_bar = [get_err(regular,   N) for N in ns_all]
            errs_d_bar = [get_err(distorted, N) for N in ns_all]

            rects_r = ax.bar([x - bar_w / 2 for x in xs],
                             [v or 0 for v in errs_r_bar], bar_w,
                             color=C_REG,  alpha=0.85, label='Regular',   zorder=3)
            rects_d = ax.bar([x + bar_w / 2 for x in xs],
                             [v or 0 for v in errs_d_bar], bar_w,
                             color=C_DIST, alpha=0.85, label='Distorted', zorder=3)

            for rect, val in (list(zip(rects_r, errs_r_bar)) +
                              list(zip(rects_d, errs_d_bar))):
                if val:
                    ax.text(rect.get_x() + rect.get_width() / 2.,
                            rect.get_height() * 1.02,
                            f'{val:.2f}%', ha='center', va='bottom',
                            fontsize=7.5)

            ax.set_xlabel('Mesh size  N', fontsize=11)
            ax.set_ylabel(ylabel, fontsize=10)
            ax.set_title(f'{comp.upper()} relative error', fontsize=11)
            ax.set_xticks(xs)
            ax.set_xticklabels([str(N) for N in ns_all])
            ax.legend(fontsize=9)
            ax.grid(True, axis='y', linestyle=':', alpha=0.4, zorder=0)

        fig.tight_layout()
        path = os.path.join(out_dir, f'error_bars{suffix}.png')
        fig.savefig(path, dpi=150, bbox_inches='tight')
        plt.close(fig)
        print(f'  Plot saved: {path}')


