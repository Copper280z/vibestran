"""
mitc4_postprocess.py
====================
Post-process Nastran SOL 101 results for the Choi & Lee (2022) clamped
plate in-plane bending benchmark generated by mitc4_plate_benchmark.py.

For each .f06 file found in the target directory the script:
  1. Reads the matching .bdf to find the Point A node ID and case metadata
     (N, mesh type, load type).
  2. Parses the F06 displacement vector for Point A (T1=U, T2=V).
  3. Selects a reference solution for error/convergence calculations:
       a. --ref-u / --ref-v  (explicit user values)
       b. --ref-from-file    (another F06 file)
       c. Auto: the regular-mesh result with the largest N >= ref_threshold
                (default 64) found in the directory — treated as fully
                converged. That case is excluded from error/convergence plots.
       d. Exact 2D elasticity formula embedded in BDF comments.
  4. Prints formatted tables (displacements, % errors, convergence slopes).
  5. Writes a plain-text summary of all displacements to results.txt.
  6. Generates convergence plots saved as PNG files.

Dependencies
------------
  matplotlib  (pip install matplotlib)
  All others: Python 3.6+ stdlib only.

Usage
-----
    python mitc4_postprocess.py results/
    python mitc4_postprocess.py results/ --ref-threshold 32
    python mitc4_postprocess.py results/ --ref-u 3.0 --ref-v -3.225
    python mitc4_postprocess.py results/ --ref-from-file results/regular_N64.f06
    python mitc4_postprocess.py results/ --no-plots
    python mitc4_postprocess.py results/ --out-dir plots/

Notes on F06 parsing
---------------------
Two header formats are recognised:
    D I S P L A C E M E N T   V E C T O R   (MSC/NX Nastran)
    D I S P L A C E M E N T S               (VIBEST and others)

Two data-row formats are accepted:
    MSC/NX:  "   123      G      1.234E+00   5.678E+00 ..."
    VIBEST:  "   123        0    1.234E+00   5.678E+00 ..."
"""

import os
import re
import sys
import math
import argparse
import textwrap
from typing import Dict, List, Optional, Tuple

# ---------------------------------------------------------------------------
# Data structures
# ---------------------------------------------------------------------------

class CaseResult:
    """Everything we know about one .f06 file."""
    def __init__(self):
        self.f06_path: str = ''
        self.bdf_path: Optional[str] = None
        self.stem: str = ''

        # Metadata parsed from BDF comments
        self.N: Optional[int] = None
        self.mesh_type: str = 'unknown'   # 'regular' or 'distorted'
        self.load_type: str = 'unknown'   # 'traction' or 'couple'
        self.alpha: Optional[float] = None
        self.seed: Optional[int] = None
        self.point_a_nid: Optional[int] = None

        # Exact elasticity values from BDF comments
        self.exact_u: Optional[float] = None
        self.exact_v: Optional[float] = None

        # Parsed from F06
        self.u: Optional[float] = None   # T1 at Point A
        self.v: Optional[float] = None   # T2 at Point A
        self.parse_error: Optional[str] = None

        # Set by pick_reference() — True means this case IS the reference
        self.is_reference: bool = False

    @property
    def sort_key(self):
        order = {'regular': 0, 'distorted': 1, 'unknown': 2}
        return (order.get(self.mesh_type, 9), self.load_type, self.N or 9999)


# ---------------------------------------------------------------------------
# BDF metadata parser
# ---------------------------------------------------------------------------

_RE_POINT_A   = re.compile(r'Point A.*?=\s*node\s+(\d+)', re.IGNORECASE)
_RE_EXACT_U   = re.compile(r'Exact\s+u_A\s*=\s*([+-]?\d[\d.eE+\-]*)', re.IGNORECASE)
_RE_EXACT_V   = re.compile(r'Exact\s+v_A\s*=\s*([+-]?\d[\d.eE+\-]*)', re.IGNORECASE)
_RE_BEAM_U    = re.compile(r'Beam-theory\s+u_A\s*=\s*([+-]?\d[\d.eE+\-]*)', re.IGNORECASE)
_RE_BEAM_V    = re.compile(r'Beam-theory\s+v_A\s*=\s*([+-]?\d[\d.eE+\-]*)', re.IGNORECASE)
_RE_MESH_N    = re.compile(r'Mesh:\s*(REGULAR|DISTORTED).*?N=(\d+)', re.IGNORECASE)
_RE_LOAD_TYPE = re.compile(r'Load type:\s*(traction|couple)', re.IGNORECASE)
_RE_ALPHA     = re.compile(r'alpha=([0-9.]+)')
_RE_SEED      = re.compile(r'seed=(\d+)')


def parse_bdf_metadata(bdf_path: str, result: CaseResult):
    try:
        with open(bdf_path, 'r') as f:
            for line in f:
                if not line.startswith('$'):
                    continue
                text = line[1:].strip()

                m = _RE_POINT_A.search(text)
                if m:
                    result.point_a_nid = int(m.group(1))

                for rx, attr in [(_RE_EXACT_U, 'exact_u'), (_RE_BEAM_U, 'exact_u'),
                                  (_RE_EXACT_V, 'exact_v'), (_RE_BEAM_V, 'exact_v')]:
                    if getattr(result, attr) is None:
                        m = rx.search(text)
                        if m:
                            setattr(result, attr, float(m.group(1)))

                m = _RE_MESH_N.search(text)
                if m:
                    result.mesh_type = m.group(1).lower()
                    result.N = int(m.group(2))

                m = _RE_LOAD_TYPE.search(text)
                if m:
                    result.load_type = m.group(1).lower()

                m = _RE_ALPHA.search(text)
                if m:
                    result.alpha = float(m.group(1))

                m = _RE_SEED.search(text)
                if m:
                    result.seed = int(m.group(1))

    except OSError as e:
        result.parse_error = (result.parse_error or '') + f' [BDF read error: {e}]'


# ---------------------------------------------------------------------------
# F06 displacement parser
# ---------------------------------------------------------------------------

_RE_DISP_HDR = re.compile(
    r'D\s+I\s+S\s+P\s+L\s+A\s+C\s+E\s+M\s+E\s+N\s+T\s+'
    r'(?:V\s+E\s+C\s+T\s+O\s+R|S\b)')

_RE_DISP_ROW = re.compile(
    r'^\s*(\d+)\s+'
    r'(?:G|\d+)\s+'
    r'([+-]?[\d.]+(?:[EeDd][+-]?\d+)?)\s+'
    r'([+-]?[\d.]+(?:[EeDd][+-]?\d+)?)')


def parse_f06_displacements(f06_path: str) -> Dict[int, Tuple[float, float]]:
    """Parse last displacement block; returns {nid: (T1, T2)}."""
    all_blocks: List[Dict] = []
    current_block: Optional[Dict] = None
    in_block = False

    try:
        with open(f06_path, 'r', errors='replace') as f:
            for line in f:
                if _RE_DISP_HDR.search(line):
                    if current_block:
                        all_blocks.append(current_block)
                    current_block = {}
                    in_block = True
                    continue

                if not in_block or current_block is None:
                    continue

                stripped = line.strip()
                if not stripped:
                    continue

                if any(kw in line for kw in
                       ('POINT ID', 'TYPE', 'GRID', 'COORD SYS',
                        'in global coordinate', 'SYS')):
                    continue

                m = _RE_DISP_ROW.match(line)
                if m:
                    nid = int(m.group(1))
                    t1  = float(m.group(2).replace('D', 'E').replace('d', 'e'))
                    t2  = float(m.group(3).replace('D', 'E').replace('d', 'e'))
                    current_block[nid] = (t1, t2)
                else:
                    if current_block:
                        if re.search(r'[A-Z]{4,}', stripped) or \
                           re.match(r'\s*\d+\s*$', line):
                            all_blocks.append(current_block)
                            current_block = {}
                            in_block = False
    except OSError:
        pass

    if current_block:
        all_blocks.append(current_block)
    return all_blocks[-1] if all_blocks else {}


